const cuisineArray = cuisineMeta.map(element => {
  return element.name;
});

const dietArray = dietMeta.map(element => {
  return element.shortDescription;
});

class MetaData {
  constructor() {
    this.allergy = allergyArray;
    this.cuisine = cuisineArray;
    this.diet = dietArray;
  }
  getSearchValue(queryArr, type) {
    if (typeof queryArr === "string") {
      queryArr = [queryArr];
    }
    const searchValue = [];
    switch (type) {
      case "cuisine":
        cuisineMeta.forEach(meta => {
          
          queryArr.forEach(query => {
            if (meta.name === query) {
              searchValue.push(meta.searchValue);
            }
          });
        });
        break;
      case "allergy":
        allergyMeta.forEach(meta => {
          queryArr.forEach(query => {
            if (meta.shortDescription === query) {
              searchValue.push(meta.searchValue);
            }
          });
        });
        break;
      case "diet":
        dietMeta.forEach(meta => {
          queryArr.forEach(query => {
            if (meta.shortDescription === query) {
              searchValue.push(meta.searchValue);
            }
          });
        });
        break;
    }
    return searchValue;
  }
}

